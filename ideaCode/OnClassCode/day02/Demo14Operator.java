public class Demo14Operator {
    public static void main(String[] args) {
        int a=30,b=20;
        a+=b;
        System.out.println(a);//50
        System.out.println(b);//20
        int c=3,d=2;
        c%=d;
        System.out.println(c);//1
        System.out.println(d);
    }
}
