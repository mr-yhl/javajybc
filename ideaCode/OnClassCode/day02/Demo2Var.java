public class Demo2Var {
    public static void main(String[] args) {
        // 定义float变量
        float a=6.6F;
        System.out.println(a);
        // 定义double变量
        double b=8.8;
        System.out.println(b);
        // 定义char变量
        char c='a';
        System.out.println(c);
        // 定义boolean变量
        boolean d=true;
        System.out.println(d);
        d=false;
        System.out.println(d);
    }
}
