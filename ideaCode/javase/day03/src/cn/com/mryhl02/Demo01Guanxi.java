package cn.com.mryhl02;

public class Demo01Guanxi {
    public static void main(String[] args) {
        //定义2个int变量,并初始化
        int a = 10;
        int b = 20;
        // boolean result = a > b;
        // System.out.println(result); // 输出false
        System.out.println(a > b); // false
        System.out.println(a >= b); // false
        System.out.println(a < b); // true
        System.out.println(a <= b); // true
        System.out.println(a != b); // true
        System.out.println(a == b); // false
        System.out.println(a = b); // false

    }
}
